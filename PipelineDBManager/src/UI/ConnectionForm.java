    private void btnConnectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConnectActionPerformed
        String database = txtDatabase.getText();
        String user = txtUser.getText();
        String password = txtPassword.getPassword().toString();
        String url = "jdbc:postgresql://localhost:5432/" + database;
        Properties props = new Properties();
        props.setProperty("user", user);
        props.setProperty("password", password);
        try{
            Connection connection = DriverManager.getConnection(url, props);
            ConnectionData connectionData = new ConnectionData(txtConnectionName.getText(), user, database, url, connection);
            parentForm.addConnection(connectionData);
        }catch (SQLException ex){
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(this, ex.getMessage(), ex.getMessage(), JOptionPane.ERROR_MESSAGE);
        }finally{
            this.dispose();
        }
    }//GEN-LAST:event_btnConnectActionPerformed

